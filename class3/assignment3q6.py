#stack implementation
stack=[2,3,0,5,1,9,10,4]
print("current stack is:",stack)
stack.append(4)
print("stack after insertion is:",stack)
del stack[1]
print("stack after deletion will become:",stack)

#queue implementation
queue=[6,7,8,9,4,5,3,2]
